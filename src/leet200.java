    public static int numIslands(char[][] grid) {
        int num = 1;
        for(int i=0;i<=grid.length-1;i++){
            for(int j=0;j<=grid[0].length-1;j++){
                if(grid[i][j]=='1'){
                    num++;
                    dfs(grid,i,j);
                }
            }

        }

        return num-1;
    }
    public static void dfs(char[][] grid,int i,int j){
        if(i<0||j<0||i> grid.length-1||j> grid[0].length-1||grid[i][j]!='1'){
            return;
        }
        grid[i][j] = '0';
        dfs(grid,i-1,j);
        dfs(grid,i+1,j);
        dfs(grid,i,j-1);
        dfs(grid,i,j+1);
    }
